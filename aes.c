static uint8_t alog(uint8_t x)
{
    uint8_t y = 1, i;

    for (i = 0; i < x; i++)
        y ^= xtime(y);

    return y;
}

static uint8_t xlog(uint8_t x)
{
    uint8_t y, i = 0;

    if ( x > 0 )
        for (i = y = 1; i > 0; i++ ) {
            y ^= xtime(y);
            if (y == x)
                break;
        }

    return i;
}

static uint8_t invmul(uint8_t x) 
{
    return ( x > 0 ) ? alog(255 - xlog(x)) : 0;
} 

static uint8_t sbox(uint8_t x)
{
    uint8_t y, sb;

    sb = y = invmul(x);
    y  = (uint8_t)((y << 1) | (y >> 7)) & 0xFF;
    sb ^= y;
    y  = (uint8_t)((y << 1) | (y >> 7)) & 0xFF;
    sb ^= y;
    y  = (uint8_t)((y << 1) | (y >> 7)) & 0xFF;
    sb ^= y;
    y  = (uint8_t)((y << 1) | (y >> 7)) & 0xFF;
    sb ^= y;

    return (sb ^ 0x63);
}

