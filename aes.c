static uint8_t xtime(uint8_t x)
{
    return ((x << 1) & 0xFF) ^ (0x1b * ((x & 0x80) >> 7) );
}


static void subBytes(uint8_t *buf)
{
    register uint8_t i;

    for (i = 0; i < 16; i++)
        buf[i] = sbox(buf[i]);

}

static void addRoundKey(uint8_t *buf, uint8_t *key)
{
    register uint8_t i;

    for (i = 0; i < 16; i++)
        buf[i] ^= key[i];

}

static void addRoundKey_2(uint8_t *buf, uint8_t *key, uint8_t *cpk)
{
    register uint8_t i = 16;

    for (i = 0; i < 16; i++) {
        cpk[i]  = key[i];
        buf[i] ^= key[i];
        cpk[16 + i] = key[16 + i];
    }

}

static void shiftRows(uint8_t *buf)
{
    register uint8_t i = buf[1], j = buf[3], k = buf[10], l = buf[14];

    buf[1]  = buf[5];
    buf[5]  = buf[9];
    buf[9]  = buf[13];
    buf[13] = i;
    buf[3]  = buf[15];
    buf[15] = buf[11];
    buf[11] = buf[7];
    buf[7]  = j;
    buf[10] = buf[2];
    buf[2]  = k;
    buf[14] = buf[6];
    buf[6]  = l;

}

static void mixColumns(uint8_t *buf)
{
    register uint8_t i, a, b, c, d, e;

    for (i = 0; i < 16; i += 4) {
        a = buf[i];
        b = buf[i + 1];
        c = buf[i + 2];
        d = buf[i + 3];
        e = a ^ b ^ c ^ d;
        buf[i]     ^= e ^ xtime(a ^ b);
        buf[i + 1] ^= e ^ xtime(b ^ c);
        buf[i + 2] ^= e ^ xtime(c ^ d);
        buf[i + 3] ^= e ^ xtime(d ^ a);
    }

}

static void expandKey(uint8_t *k, uint8_t rc)
{
    register uint8_t i;

    k[0] ^= sbox(k[29]) ^ rc;
    k[1] ^= sbox(k[30]);
    k[2] ^= sbox(k[31]);
    k[3] ^= sbox(k[28]);

    for(i = 4; i < 16; i += 4) {
        k[i]     ^= k[i - 4];
        k[i + 1] ^= k[i - 3];
        k[i + 2] ^= k[i - 2];
        k[i + 3] ^= k[i - 1];
    }
    k[16] ^= sbox(k[12]);
    k[17] ^= sbox(k[13]);
    k[18] ^= sbox(k[14]);
    k[19] ^= sbox(k[15]);

    for(i = 20; i < 32; i += 4) {
        k[i]     ^= k[i - 4];
        k[i + 1] ^= k[i - 3];
        k[i + 2] ^= k[i - 2];
        k[i + 3] ^= k[i - 1];
    }

} 

void aes_init(aes_ctx *ctx, uint8_t *k)
{
    register uint8_t i;

    for (i = 0; i < sizeof(ctx->key); i++)
        ctx->ek[i] = k[i];

}

void encrypt(aes_ctx *ctx, uint8_t *buf)
{
    uint8_t i, rcon = 1;

    addRoundKey_2(buf, ctx->ek, ctx->key);
    for(i = 1; i < 14; ++i) {
        subBytes(buf);
        shiftRows(buf);
        mixColumns(buf);
        if( (i & 1) == 1 )
            addRoundKey(buf, &ctx->key[16]);
        else {
            expandKey(ctx->key, rcon);
            rcon = xtime(rcon);
            addRoundKey(buf, ctx->key);
        }

    }
    subBytes(buf);
    shiftRows(buf);
    expandKey(ctx->key, rcon);
    addRoundKey(buf, ctx->key);

}

static void incrementCtr(uint8_t *val)
{
    if ( val != NULL )
        if ( ++val[15] == 0 )
            if ( ++val[14] == 0 )
                if ( ++val[13] == 0 )
                    val[12]++;

}

static void ctr_KS(aes_ctx *ctx, uint8_t *ks)
{
    uint8_t i;
    uint8_t *p = (uint8_t *)&ctx->blk;

    if ( (ctx != NULL) && (ks != NULL) ) {
        for (i = 0; i < sizeof(ctx->blk); i++)
            ks[i] = p[i];

        encrypt(ctx, ks);
        incrementCtr(&ctx->blk.iv[0]);
    }

}

void init_ctr(aes_ctx *ctx, iv_blk *blk)
{
    uint8_t i, *p = (uint8_t *)&ctx->blk, *v = (uint8_t *)blk;

    if ( (ctx != NULL) && (blk != NULL) )
        for (i = 0; i < sizeof(ctx->blk); i++)
            p[i] = v[i];

}

void ctr_encrypt(aes_ctx *ctx, uint8_t *buf, size_t sz)
{
    uint8_t key[sizeof(ctx->blk)];
    size_t  i;
    uint8_t j = sizeof(key);

    for (i = 0; i < sz; i++) {
        if ( j == sizeof(key) ) {
            j = 0;
            ctr_KS(ctx, key);
        }
        buf[i] ^= key[j++];
    }

}


